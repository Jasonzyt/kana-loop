export enum OtoType {
  seion = "seion",
  dakuon = "dakuon",
  youon = "youon",
}

export type Oto = {
  // 音 おと
  hira: string;
  kana: string;
  romaji: string;
};

export const HIRA_TO_OTO_TYPE = (function () {
  let result: Record<string, OtoType> = {};
  for (let i = 0; i < SEION_LIST.length; i++) {
    result[SEION_LIST[i].hira] = OtoType.seion;
  }
  for (let i = 0; i < DAKUON_LIST.length; i++) {
    result[DAKUON_LIST[i].hira] = OtoType.dakuon;
  }
  for (let i = 0; i < YOUON_LIST.length; i++) {
    result[YOUON_LIST[i].hira] = OtoType.youon;
  }
  return result;
})();

export const HIRA_TO_OTO = (function () {
  let result: Record<string, Oto> = {};
  for (let i = 0; i < SEION_LIST.length; i++) {
    result[SEION_LIST[i].hira] = SEION_LIST[i];
  }
  for (let i = 0; i < DAKUON_LIST.length; i++) {
    result[DAKUON_LIST[i].hira] = DAKUON_LIST[i];
  }
  for (let i = 0; i < YOUON_LIST.length; i++) {
    result[YOUON_LIST[i].hira] = YOUON_LIST[i];
  }
  return result;
})();

